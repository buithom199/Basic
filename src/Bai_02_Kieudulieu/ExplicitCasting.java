package Bai_02_Kieudulieu;

public class ExplicitCasting {

	public static void main(String[] args) {
		// Ép kiểu hẹp
		double d = 100.04;
		long l = (long) d;
		int i = (int) l;
		System.out.println("Giá trị biến d =" + d);
		System.out.println("Giá trị biến l =" + l);
		System.out.println("Giá trị kiểu i =" + i);
		

	}

}
